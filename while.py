"""
This program will continue adding 10 to i starting from i = 0
and stopping once the value of i exceeds 119. It will print
each result on a newline.
"""
i = 0           # define variable i as an integer

while(i<119):   # while i is less than 119, do the following
    print(i)    # print the current value of i
    i += 10     # add 10 to i
